# 如何使用數字、數字的用法
from math import *
number = 8
print(number*5)
print((8+8)*5)
print(number%5)
print("會印出數字 " + str(number))
print(abs(number)) #絕對值
print(pow(number,2)) #次方
print(max(1,2,5,6,9,10))
print(round(4.4))
print(round(4.6)) #四捨五入
print(floor(4.6)) #無條件捨去
print(ceil(5.1)) #無條件進位
print(sqrt(64)) #開根號
